function clickColor(){
    if(event.target.className == 'col1'){
       event.target.style.color = "red";
    }
    if(event.target.className == 'col2'){
        event.target.style.color = "yellow";
    }
    if(event.target.className == 'col3'){
        event.target.style.color = "green";
    }
    if(event.target.className == 'col4'){
        event.target.style.color = "blue";
    }
    if(event.target.className == 'col5'){
        event.target.style.color = "orange";
    }
    if(event.target.className == 'col6'){
        event.target.style.color = "gray";
    }
}

